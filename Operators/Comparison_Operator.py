#3. # COMPARISON OPERATORS :- USED TO COMPARE TWO VALUES AND ALWAYS RETURN A BOOLEAN VALUE (TRUE / FALSE).
# EX: ==, !=, >, <, >=, <=

first_number = 10
second_number = 20

print("output:", first_number == second_number)      # False
print("output:", first_number != second_number)      # True
print("output:", first_number > second_number)       # False
print("output:", first_number < second_number)       # True
print("output:", first_number >= second_number)      # False
print("output:", first_number <= second_number)      # True



